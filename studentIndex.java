    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollno = jTextField4.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Mysql2023may");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student inner join result where student.rollno='"+rollno+"' and result.rollno='"+rollno+"'");
            if(rs.next())
            {
                jTextField1.setText(rs.getString(2));
                jTextField2.setText(rs.getString(3));
                jTextField3.setText(rs.getString(4));
                jTextField5.setText(rs.getString(6));
                jTextField6.setText(rs.getString(5));
                jTextField17.setText(rs.getString(8));
                jTextField18.setText(rs.getString(9));
                jTextField19.setText(rs.getString(10));
                jTextField20.setText(rs.getString(11));
                jTextField21.setText(rs.getString(12));
       
                
                int s1=Integer.parseInt(jTextField17.getText());
                int s2=Integer.parseInt(jTextField18.getText());
                int s3=Integer.parseInt(jTextField19.getText());
                int s4=Integer.parseInt(jTextField20.getText());
                int s5=Integer.parseInt(jTextField21.getText());
                
                String totalMarks = String.valueOf(s1+s2+s3+s4+s5);
                jTextField22.setText(totalMarks);
                
                
                
                
            }
            
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_formComponentShown

